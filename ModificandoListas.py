#modificando elementos de una lista

print("Modificando vocales[1] = 'x'")
vocales = ["a","e","i","o","u"]
print(f"Lista vocales: {vocales}")
vocales[1] = "x"
print(f"Lista vocales: {vocales}\n")


print("Modificando vocales[1] = 2")

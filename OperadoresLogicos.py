#Operadores lógicos
numero = int(input("Bienvenido, ingresa un número para realizar comapraciones varias: "))

if numero != 15 and numero > 10 :
    print("-El número ingresado no es 15 y es mayor a 10")
if numero == 5 or numero == 10 :
    print("-El número ingresado es 5 o 10")
if not numero == 20:
    print("-El numero ingresado no es 20")

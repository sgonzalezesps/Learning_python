#Tipos de datos

"tipo enetero o long"
numero = 15
print(numero, type(numero))

"tipo float"
numeroDecimal = 4.5
print(numeroDecimal, type(numeroDecimal))

"tipo complejo"
numeroComplejo = 5 + 6j
print(numeroComplejo, type(numeroComplejo))

"tipo string"
nombre = "Sebas"
print(nombre, type(nombre))

"tipo booleano"
verdadero_falso = 3 == 3
print(verdadero_falso, type(verdadero_falso))

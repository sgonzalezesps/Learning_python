#Operadores relacionales
numero = int(input("Bienvenido, ingresa un número para compararlo con el número 5: "))

if numero == 5:
    print("-El número ingresado es igual a 5")
if numero != 5 :
    print("-El número ingresado es diferente de 5")
if numero > 5:
    print("-El numero ingresado es mayor a 5")
if numero < 5:
    print("-El número ingresado es menor a 5")
if numero >= 5:
    print("-El numero ingresado es mayor o igual a 5")
if numero <= 5:
    print("-El número ingresado es menor o igual a 5")
